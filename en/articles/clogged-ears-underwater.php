<p>"Like I have cotton wool in my ears" or "I can hear as if underwater" — this feeling is very common and usually more harmless than tinnitus. But it is important to know when it is simply an annoyance and when it is a signal to act on urgently.</p>

<h2>What the "blockage" really is</h2>
<p>The feeling of fullness or pressure in the ear usually comes from the <strong>Eustachian tube</strong> — the slender canal that balances pressure between the middle ear and throat. When it becomes blocked (cold, allergy, sinusitis, plane landing), the ear feels "full" and sounds become muffled. It is different from tinnitus, which is a <em>sound</em>, not a pressure.</p>

<h2>Common and harmless causes</h2>
<ul>
  <li><strong>After a cold or allergy</strong> — the swelling blocks the tube; it passes in a few days or one to two weeks.</li>
  <li><strong>Earwax</strong> — accumulated wax makes sound muffled and can sometimes intensify tinnitus; it is easily removed by a doctor.</li>
  <li><strong>Pressure changes</strong> — aeroplane, mountain; usually equalised by yawning or swallowing.</li>
</ul>

<h2>What helps at home</h2>
<p>For blockage from a cold, <strong>swallowing, yawning or gentle blowing with a pinched nose</strong> (Valsalva manoeuvre, with care) often helps. <strong>Do not</strong> put cotton buds in the ear — they push the wax deeper. Earwax is removed by a specialist.</p>

<h2>When to act quickly</h2>
<p>Here is the one important exception. See a doctor <strong>immediately</strong> if the blockage is <strong>sudden</strong>, in <strong>one ear only</strong> and with <strong>hearing loss</strong> or strong dizziness: it could be <strong>sudden sensorineural hearing loss</strong>, which has a narrow treatment window.</p>

<h2>In short</h2>
<p>A blocked ear is usually a matter of pressure or earwax — annoying but temporary. Watch out only for the sudden blockage in one ear with hearing loss: that is an emergency. In all other cases you have time to let it pass or show it calmly to a doctor.</p>
