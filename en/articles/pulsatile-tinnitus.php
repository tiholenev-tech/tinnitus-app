<h2>How it differs from ordinary tinnitus</h2>
<p>Most tinnitus is <strong>subjective</strong>: the sound is generated in the auditory system and has no physical source. Pulsatile tinnitus is different — it is usually a <strong>real sound</strong>, caused by the movement of blood through a vessel near the middle or inner ear. That is why sometimes the doctor can hear it with a stethoscope.</p>

