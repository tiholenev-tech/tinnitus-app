<p>Caffeine does not worsen tinnitus. In a prospective study of <span class="num">65,085</span> women followed for <span class="num">18</span> years, those who consumed more caffeine had a <em>lower</em> risk of tinnitus, not higher (Glicksman, 2014). Abruptly stopping coffee can also cause withdrawal headaches and irritability — which certainly do not help. So: if you enjoy it, you can continue.</p>

<h2>Alcohol: it depends on you</h2>
<p>With alcohol, the story is different and very personal. In some people a drink triggers or temporarily increases the ringing, in others nothing changes. But there is a more consistent and often underestimated effect: alcohol fragments sleep. And poor sleep is one of the things that makes tinnitus more present and more bothersome the next day.</p>

