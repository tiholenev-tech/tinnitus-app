<p>The instinct, faced with tinnitus, is to fight it: to check it, cover it, wish it away. Mindfulness proposes the opposite — and that is precisely why it works. It is not eastern magic, it is an attention practice with scientific foundations.</p>

<h2>What changes: the relationship, not the intensity</h2>
<p>Mindfulness does not act on the sound, but on the reaction to the sound. When you stop fighting the ringing, the brain stops treating it as a threat and strips it of significance. The sound is still there, but it slips into the background, like the hum of a fridge you no longer notice.</p>

<h2>What studies show</h2>
<p>Mindfulness-Based Cognitive Therapy (MBCT) reduces tinnitus distress as much as or more than classical relaxation, with an improvement maintained at six months (McKenna, 2017). It belongs to the same family as cognitive behavioural therapy, which reduces tinnitus distress by <span class="num">10.91</span> THI points (Cochrane review, 2,733 patients) — the strongest psychological evidence available for tinnitus.</p>

<h2>A simple daily practice</h2>
<ul>
  <li><strong>Observe without judging.</strong> Bring your attention to the ringing for a few moments, observing it as "a sound", without labels like "unbearable".</li>
  <li><strong>Shift focus.</strong> Widen attention to other sounds and bodily sensations: tinnitus becomes one of many, not the centre.</li>
  <li><strong>Breathe slowly.</strong> Long exhales calm the nervous system and reduce the alarm.</li>
  <li><strong>A few minutes, often.</strong> Three brief sessions a day are worth more than one long one occasionally.</li>
</ul>

<h2>Who benefits most</h2>
<p>Mindfulness helps mainly those who experience the ringing with intensity, anxiety or hypervigilance. It does not replace a medical evaluation when there are warning signs (tinnitus in one ear, pulsatile, sudden onset), but it is an excellent daily tool to regain control.</p>

<h2>In short</h2>
<p>Mindfulness does not silence tinnitus: it takes away its power. By training attention not to fight the sound, you reduce anxiety and distress — and the ringing moves to the background. A few minutes a day, consistently, is the key.</p>
