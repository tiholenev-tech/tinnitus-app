<h2>In cosa è diverso dall'acufene comune</h2>
<p>La maggior parte dell'acufene è <strong>soggettivo</strong>: il suono nasce nel sistema uditivo e non ha una fonte fisica. L'acufene pulsante è diverso — di solito è un <strong>suono reale</strong> dovuto al movimento del sangue in un vaso vicino all'orecchio medio o interno. Per questo a volte il medico può sentirlo con lo stetoscopio.</p>

