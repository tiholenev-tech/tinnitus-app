<p>La mindfulness non agisce sul suono ma sulla reazione al suono. Quando smetti di lottare con il fischio, il cervello smette di trattarlo come una minaccia e gli toglie importanza. Il suono c'è ancora, ma scivola sullo sfondo, come il ronzio del frigorifero che non noti più.</p>

