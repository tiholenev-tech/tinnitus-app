<h2>Ce se schimbă: relația, nu volumul</h2>
<p>Mindfulness nu acționează asupra sunetului, ci asupra reacției la sunet. Când încetezi să lupți cu țiuitul, creierul încetează să-l trateze ca pe o amenințare și îi ia importanța. Sunetul este încă acolo, dar alunecă în fundal, ca bâzâitul frigiderului pe care nu-l mai observi.</p>

