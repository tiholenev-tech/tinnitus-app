mp = Path('audio/library/manifest.json')
m = json.loads(mp.read_text(encoding='utf-8'))
renamed = 0
for x in m['sounds']:
    old = x.get('bg_title')
    if old in MAP:
        x['bg_title'] = MAP[old]; renamed += 1

# dedup safety
QUAL = ['по здрач', 'на разсъмване', 'отблизо', 'в далечината', 'привечер', 'нощем']
seen = {}
for x in m['sounds']:
    t = x.get('bg_title') or ''
    if t in seen:
        seen[t] += 1
        x['bg_title'] = t + ' · ' + QUAL[(seen[t]-2) % len(QUAL)]
    else:
        seen[t] = 1

mp.write_text(json.dumps(m, ensure_ascii=False, indent=1), encoding='utf-8')
print('renamed:', renamed, 'of', len(MAP), 'mapped')
# покажи дубликати ако са останали
dups = [k for k, v in seen.items() if v > 1]
